package com.example.spacechase.utils;

/**
 * Controls of player.
 * @author Tristan Tsang
 * @version 1.0.0
 */
public enum Control {
    /**
     * Upward movement.
     */
    UP,
    /**
     * Left movement.
     */
    LEFT,
    /**
     * Right movement.
     */
    RIGHT,
    /**
     * Downward movement.
     */
    DOWN,
    /**
     * Null control.
     */
    NULL
}
